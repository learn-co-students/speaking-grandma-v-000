

def speak_to_grandma(speak)
  
  if speak == "I LOVE YOU GRANDMA!"
    "I LOVE YOU TOO PUMPKIN!"
    
  elsif speak != speak.upcase
    "HUH?! SPEAK UP, SONNY!"
    
  elsif speak == "WHAT DID YOU EAT TODAY?"
    "NO, NOT SINCE 1938!"
  
  elsif speak == "WHAT?"
    "NO, NOT SINCE 1938!"
    
  end 
end 
    
    